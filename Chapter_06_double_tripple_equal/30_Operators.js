//Assignment operators
// - =, +=, -=, *=, /=, %=
// to assign values to variables

let a = 10; // assignment operator
x = 11; // assignment operator
x = x + 1; // addition assignment operator
x += 1; // addition assignment operator, equivalent to x = x + 1
x -= 2; // subtraction assignment operator, equivalent to x = x - 2
x *= 3; // multiplication assignment operator, equivalent to x = x * 3
x /= 2; // division assignment operator, equivalent to x = x / 2
x %= 4; // modulus assignment operator, equivalent to x = x % 4 

